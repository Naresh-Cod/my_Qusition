


# adding and Subtracting
# Matrices in Python
# using numpy
import numpy as np

# Creating first matrix
first = np.array([[16, 5], [44, 4]])

# creating second matrix
sec = np.array([[34, 55], [89, 60]])

print("First matrix")
print(first)
print("Matrix")
print(sec)

# two matrix
print("Subtraction of two matrix")
print(np.subtract(first, sec))

